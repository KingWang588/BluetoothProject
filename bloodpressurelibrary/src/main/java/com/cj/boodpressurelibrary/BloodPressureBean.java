package com.cj.boodpressurelibrary;

public class BloodPressureBean {
    private String systolicPressure;//收缩压
    private String diastolicPressure;//舒张压
    private String heartRate;//心率
    private String HeartFibrillation;//心颤
    private String year;//年
    private String month;//月
    private String day;//日
    private String hour;//时
    private String minute;//分
    private String group;//组别

    public String getSystolicPressure() {
        return systolicPressure;
    }

    public void setSystolicPressure(String systolicPressure) {
        this.systolicPressure = systolicPressure;
    }

    public String getDiastolicPressure() {
        return diastolicPressure;
    }

    public void setDiastolicPressure(String diastolicPressure) {
        this.diastolicPressure = diastolicPressure;
    }

    public String getHeartRate() {
        return heartRate;
    }

    public void setHeartRate(String heartRate) {
        this.heartRate = heartRate;
    }

    public String getHeartFibrillation() {
        return HeartFibrillation;
    }

    public void setHeartFibrillation(String heartFibrillation) {
        HeartFibrillation = heartFibrillation;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getHour() {
        return hour;
    }

    public void setHour(String hour) {
        this.hour = hour;
    }

    public String getMinute() {
        return minute;
    }

    public void setMinute(String minute) {
        this.minute = minute;
    }

    public String getGroup() {
        return group;
    }

    public void setGroup(String group) {
        this.group = group;
    }
}
